/// Renders `text` as big block-letter ASCII art, one row per output line.
/// Mirrors the look of tacoproz1/tacos-terminal-lyrics: a single line of
/// lyrics blown up to fill the terminal instead of a scrolling list.
pub fn render(text: &str) -> Vec<String> {
    let chars: Vec<char> = text.chars().collect();
    let mut rows = vec![String::new(); HEIGHT];

    for (i, &ch) in chars.iter().enumerate() {
        let g = glyph(ch);
        for (row, slice) in g.iter().enumerate() {
            rows[row].push_str(slice);
        }
        if i + 1 != chars.len() {
            for row in rows.iter_mut() {
                row.push(' ');
            }
        }
    }

    rows
}
